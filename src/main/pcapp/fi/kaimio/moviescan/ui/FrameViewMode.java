/**
 Different modes in which a frame can be viewed
 */
public enum FrameViewMode {
    
    /**
     Draw the frame according to current crop settings
     */
    DRAW_FRAME,
    
    /**
     Draw the perforation in 100 % scale & allow editing it
     */
    DRAW_PERFORATION
}
